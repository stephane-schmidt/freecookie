	/**
	 * Détermine la langue à servir (cascade).
	 *
	 * 1) langue active WPML/Polylang (langue réelle de la PAGE affichée)
	 * → 2) (option) langue du NAVIGATEUR du visiteur, si prise en charge
	 * → 3) locale du site → 4) anglais.
	 *
	 * @param bool $use_browser La langue du navigateur prime sur celle du site.
	 * @return string Code court : fr, en, de, it…
	 */
	public static function detect( $use_browser = false ) {
		// Polylang.
		if ( function_exists( 'pll_current_language' ) ) {
			$pll = pll_current_language( 'slug' );
			if ( $pll ) {
				return self::normalize( $pll );
			}
		}
		// WPML.
		if ( defined( 'ICL_LANGUAGE_CODE' ) && ICL_LANGUAGE_CODE ) {
			return self::normalize( ICL_LANGUAGE_CODE );
		}
		// Navigateur du visiteur : première langue de sa liste que l'on parle.
		if ( $use_browser ) {
			$bl = self::browser_lang();
			if ( '' !== $bl ) {
				return $bl;
			}
		}
		// Locale du site.
		$locale = get_locale();
		if ( $locale ) {
			return self::normalize( $locale );
		}
		return 'en';
	}

	/**
	 * Première langue PRISE EN CHARGE de la liste Accept-Language du visiteur
	 * (parcourue dans l'ordre de préférence), ou '' si aucune.
	 *
	 * @return string Code court ou ''.
	 */
	public static function browser_lang() {
		if ( empty( $_SERVER['HTTP_ACCEPT_LANGUAGE'] ) ) {
			return '';
		}
		$al    = strtolower( sanitize_text_field( wp_unslash( $_SERVER['HTTP_ACCEPT_LANGUAGE'] ) ) );
		$avail = array_keys( self::strings() );
		foreach ( explode( ',', $al ) as $part ) {
			$code   = trim( explode( ';', $part )[0] );
			$mapped = self::map_code( str_replace( '_', '-', $code ) );
			if ( in_array( $mapped, $avail, true ) ) {
				return $mapped;
			}
		}
		return '';
	}

	/**
	 * Réduit un code de locale (minuscule, tirets) à un code de langue du bandeau.
	 * 2 lettres pour la plupart ; RÉGION-CONSCIENT pour le portugais et le chinois
	 * (variantes non interchangeables), et repli norvégien.
	 *
	 * @param string $code Locale minuscule à tirets (ex. « pt-br », « zh-hant », « fr-fr »).
	 * @return string Code court Freecookie_I18n (ex. « pt-PT », « zh-CN », « fr »).
	 */
	protected static function map_code( $code ) {
		if ( 0 === strpos( $code, 'pt' ) ) {
			return ( 0 === strpos( $code, 'pt-br' ) ) ? 'pt-BR' : 'pt-PT'; // pt nu → européen.
		}
		if ( 0 === strpos( $code, 'zh' ) ) {
			return preg_match( '/zh-(hant|tw|hk|mo)/', $code ) ? 'zh-TW' : 'zh-CN'; // zh nu → simplifié.
		}
		if ( 0 === strpos( $code, 'nb' ) || 0 === strpos( $code, 'nn' ) || 0 === strpos( $code, 'no' ) ) {
			return 'nb'; // norvégien (bokmål / nynorsk / générique).
		}
		return substr( $code, 0, 2 );
	}

	/**
	 * Réduit « fr_FR », « fr-CH », « FR » → « fr ».
	 *
	 * @param string $code Code de langue.
	 * @return string
	 */
	public static function normalize( $code ) {
		$code   = strtolower( (string) $code );
		$code   = str_replace( '_', '-', $code );
		$mapped = self::map_code( $code );
		$avail  = array_keys( self::strings() );
		return in_array( $mapped, $avail, true ) ? $mapped : 'en';
	}

	/**
	 * Renvoie les chaînes du bandeau pour une langue (repli anglais).
	 *
	 * @param string $lang Code court.
	 * @return array<string,string>
	 */
	public static function get( $lang ) {
		$all = self::strings();
		if ( isset( $all[ $lang ] ) ) {
			return $all[ $lang ];
		}
		$base = substr( (string) $lang, 0, 2 );
		if ( isset( $all[ $base ] ) ) {
			return $all[ $base ];
		}
		return $all['en'];
	}

	/**
	 * Sélectionne la variante d'une valeur multilingue (repli anglais puis première).
	 *
	 * @param string|array<string,string> $value Chaîne unique ou tableau lang => texte.
	 * @param string                      $lang  Code court.
	 * @return string
	 */
	public static function pick( $value, $lang ) {
		if ( ! is_array( $value ) ) {
			return (string) $value;
		}
		if ( isset( $value[ $lang ] ) ) {
			return $value[ $lang ];
		}
		// Repli d'une variante régionale sur sa base : pt-PT → pt, zh-CN → zh.
		// Utile pour les tables encore en 7 langues (finalités, fiches cookies).
		$base = substr( (string) $lang, 0, 2 );
		if ( $base !== $lang && isset( $value[ $base ] ) ) {
			return $value[ $base ];
		}
		if ( isset( $value['en'] ) ) {
			return $value['en'];
		}
		$first = reset( $value );
		return is_string( $first ) ? $first : '';
	}

